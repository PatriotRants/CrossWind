namespace ForgeWorks.CrossWind.Presentation;

/// <summary>
/// Window visibility property
/// </summary>
public enum Visibility
{
    /// <summary>
    /// UYnknown visibility; the DEFAULT value
    /// </summary>
    Unknown = -1,
    /// <summary>
    /// Window is hidden
    /// </summary>
    Hidden = 0,
    /// <summary>
    /// Window is visible
    /// </summary>
    Visible = 1,
}
